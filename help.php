<h2 id="home">TheMusicSearch</h2>

<h3 id="columnschanges:done" >TheLibrary</h3>

<p> Searching for music on Radiolize is as easy as it can get. With a library comprising of over 15+ million songs, its hard that you won't find the song you're looking for.
<ul>
<li ><strong>Step 1: </strong>Just search for any song you like in the "Search Music" textbox under the Library.</li>
<li ><strong>Step 2: </strong>Play any song on the loaded list by clicking on them.</li>
<li ><strong>Download: </strong> To download any song just get your cursor over the link and when it gets <strong>"underlined"</strong>, Right Click => Save Link As.. Give your file some meaningful name..and you're done.</li>
<li ><strong>Tip: </strong>There might be times when you might not be able to get exactly the song you're looking for. When this happens, just try modifying your search keyword e.g "its like that mariah carey" To "mariah carey its like that" , or just choose the appropriate option from the drop down menu.</li>
</ul>

