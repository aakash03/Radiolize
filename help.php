<h2>TheRadio</h2>

<h3>Starting Your Radio Station</h3>
<p><b>NOTE: </b>In order to get started with your Radio you must have atleast one playlist with over "3 songs".</p>
<p>Once you've created a playlist containing over 3 songs you're all set to have your own radio station LIVE which people can listen to anytime of the day :D</p>
<ul>
<li>Head onto the Radio Section by clicking on "Radio" in the sidebar</li>
<li>Select the playlist(s) which you want to stream under the "Control Panel".</li>
<li>Get started by clicking on the on/off slider button to switch "On" your Radio.</li>
<li>Once your Radio is up, go to your profile (via the <b>Socialize</b> Menu Option) and hit "<b>Play Radio</b>"</li>
</ul>

<h3>Dynamic Radio Playlist Updation</h3>
<ul style="list-style: none;" >
<li> You can modify the songs to come on your radio under the "Upcoming Songs" section.</li>
<li>The "Upcoming Song" section shows the next 5 songs to come.</li>
<li>New songs can be added to this list from the library or the old ones modified (rearranged. deleted etc)</li>
</ul>

<h3>Things To Know About Your Radio</h3>
<ul>
<li>Your Radio comprises of the circular-queued playlist which goes-on-and-on forever. </li>
<li>No matter whether you're online or not your Radio will stay "On" forever, and people can listen to it anytime.</li>
<li>Your Radio Broadcasting costs you 0% bandwidth, i.e you are not streaming anything from "your PC"</li>
<li>Finally, Having an Online Radio Station of your own makes you Cool B)</li>

